""" Escreva um programa que escreva na tela de 1 at√© 100, de 1 em 1, 2 vezes. A primeira deve ser usando a estrutura for,
a segunda utilizando while."""
# Com for
for contador in range(1, 101):
    print(contador)
numero = 1
# Com While
while numero != 101:
    print(numero)
    numero = numero + 1
